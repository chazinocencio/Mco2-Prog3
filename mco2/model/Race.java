package model;
/**
 * Race.java
 *
 * Defines the playable character races available in the game,
 * each with unique traits and abilities.
 */
public enum Race {
    /** Human: Adaptable and resilient, humans possess a balanced set of attributes. */
    Human,
    /** Dwarf: Stocky and tough, dwarves are known for their incredible endurance and steadfastness. */
    Dwarf,
    /** Elf: Graceful and naturally attuned to arcane energies, elves excel in precise actions and magical prowess. */
    Elf,
    /** Gnome: Clever and resourceful, gnomes have a knack for finding hidden opportunities or leveraging unusual tricks. */
    Gnome;
}
